#string methods
s1 = "HELLO"
s2 = "hello"
s3 = "How Are You"
s4 = "hello1"

#retuns true if all letters are in upper case
print(s1.isupper())

#retuns true if all letters are in lower case
print(s2.islower())

#if all letters of every word if uppercase
print(s3.istitle())

#checks if string contains alphanumeric characters
print(s4.isalnum())

#checks if string contains only alphabets
print(s1.isalpha())
